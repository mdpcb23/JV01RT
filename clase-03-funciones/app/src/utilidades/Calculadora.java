package utilidades;

public class Calculadora {

    public static double sumarDoubles(double a, double b){
        return a + b;
    }

    public static long sumarLong(long a, long b){
        return a + b;
    }

    public static double sumarDoubles(double a, double b, double c){
        return a + b + c;
    }

    public static double restarDouble(double num1, double num2){
        return num1 - num2;
    }

    public static double multiplicarDouble(double num1, double num2){
        return num1 * num2;
    }

    public static double divDouble(double num1, double num2){
        return num1 / num2;
    }


}
