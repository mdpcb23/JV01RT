public class Main {
    public static void main(String[] args) {
        System.out.println("Hello, World!");

        // public: Que significa?
        //        Es accesible desde cualquier punto de mi proyecto para la JVM = Java Virtual Machine.

        // static: Que significa?
        //       que pertenece a la clase Main y no a una instancia unica.

        // void: Que significa?
        //       que no retorna ningun valor, que esta vacio.

        // main: Que significa?
        //       este es el no,bre obligatoria para JVM para lograr ejecutar tu proyecto.

        // String[]: Que significa?
        //       es un array de string.

        System.out.println("lo que yo quiera");

        //snippet SOUT
        System.out.println();





    }
}